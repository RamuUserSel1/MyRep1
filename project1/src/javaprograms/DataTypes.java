package javaprograms;

public class DataTypes {

	public static void main(String[] args) {
		int a =5;
		
		System.out.println("Integer a value="+a);
		
		double b=45.89;
		
		System.out.println("Double b value ="+b);
		
		char c='C';
		
		System.out.println("Character c value="+c);
		
		String course ="Selenium";
		
		System.out.println("String course value="+course);
		
		boolean flag=true;
		
		System.out.println("Boolean flag value="+flag);
		
		

	}

}
